     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'Categoriename'     =>  'required',
            'Categorieimage'    =>  'required'
        ]);
        $addCategorie = new AddCategorie([
            'market_selection'  =>  $request->get('marketSelect'),
            'Categorie_name'    =>  $request->get('Categoriename'),
            'Categorie_image'   =>  $request->get('Categorieimage')
            ]);
        $addCategorie->save();
        return redirect()->route('Admin.Add_Categorie')->with('success', 'Data Added');
    }

